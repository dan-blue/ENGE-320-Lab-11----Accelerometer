//=============================================================================
int main(void)
{
	uint32_t old_millis = 0;
	/* Initialize the SAM system */
	SystemInit();
	i2c_init();
	accelerometer_init();
	SysTick_Config(48000); // every ms
	
	while (1)
	{
		if ((millis - old_millis) > 49)
		{
			if (accelerometer_update())
			{
				__NOP();
			}
			old_millis = millis;
		}
		uint16_t testX = accelerometer_getX();
		uint16_t testY = accelerometer_getY();
			
	}
}


